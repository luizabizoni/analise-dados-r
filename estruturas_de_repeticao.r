# For: utilizada para acessar cada elemento de uma sequência
## for (valor in sequência) {
##     código
## }

for (i in seq(12)){
    print(i)
}

# While: utilizada para repetir uma operação por um determinado número de vezes
## while (condição) {
##     código
## }

i = 0
while (i <= 20) {
    print(i)
    i = i+1
}